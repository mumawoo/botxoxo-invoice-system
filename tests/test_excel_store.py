def _write_manual_records(path: Path, records: list[InvoiceRecord]) -> None:
    wb = Workbook()
    ws = wb.active
    ws.append(EXCEL_HEADERS)
    for index, record in enumerate(records, start=1):
        record.line_no = index
        ws.append(record.to_excel_row())
    wb.save(path)
    wb.close()


