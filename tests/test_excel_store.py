    def test_run_summary_does_not_double_count_locked_duplicate_input(self):
        with tempfile.TemporaryDirectory() as temp:
            temp_path = Path(temp)
            manual = temp_path / "Invoice_Manually_Checked.xlsx"
            locked = InvoiceRecord(invoice_date="2026-06-12", seller="Cafe", total_amount=10, status="Manually checked")
            _write_manual_records(manual, [locked])

            out = temp_path / "Invoice_Output.xlsx"
            store = InvoiceWorkbook(out, manual)
            written = store.write_records(
                [
                    InvoiceRecord(invoice_date="2026-06-12", seller="Cafe", total_amount=10),
                    InvoiceRecord(invoice_date="2026-06-13", seller="Market", total_amount=20),
                ],
                [
                    OCRAuditRow("source.jpg", "crop1.jpg", "local OCR agreement", False),
                    OCRAuditRow("source.jpg", "crop2.jpg", "local OCR agreement", False),
                ],
            )

            self.assertEqual(written, 1)
            wb = load_workbook(out, data_only=True)
            summary = {row[0]: row[1] for row in wb["Run_Summary"].iter_rows(values_only=True) if row[0]}
            self.assertEqual(summary["Invoice rows"], 2)
            self.assertEqual(summary["New rows written"], 1)
            self.assertEqual(summary["Locked rows preserved"], 1)
            self.assertEqual(wb["Invoices"].max_row, 3)
            wb.close()

