import tempfile
import unittest
from pathlib import Path

from openpyxl import Workbook, load_workbook

from invoice_system.excel_store import InvoiceWorkbook, load_invoice_records
from invoice_system.models import EXCEL_HEADERS, InvoiceRecord, OCRAuditRow, SourceQARecord


class ExcelStoreTests(unittest.TestCase):
    def test_writes_13_headers(self):
        with tempfile.TemporaryDirectory() as temp:
            out = Path(temp) / "Invoice_Output.xlsx"
            store = InvoiceWorkbook(out)
            store.write_records([InvoiceRecord(invoice_date="2026-06-12", seller="Cafe", total_amount=10)])
            wb = load_workbook(out)
            headers = [cell.value for cell in wb.active[1]]
            self.assertEqual(headers, EXCEL_HEADERS)
            wb.close()

    def test_load_invoice_records_uses_invoices_sheet_even_when_not_active(self):
        with tempfile.TemporaryDirectory() as temp:
            path = Path(temp) / "ubuntu_output.xlsx"
            wb = Workbook()
            summary = wb.active
            summary.title = "Summary"
            summary.append(["Metric", "Value"])
            ws = wb.create_sheet("Invoices")
            ws.append(EXCEL_HEADERS)
            record = InvoiceRecord(invoice_date="2026-06-12", seller="Cafe", total_amount=10)
            record.line_no = 1
            ws.append(record.to_excel_row())
            wb.save(path)
            wb.close()

            rows = load_invoice_records(path)

            self.assertEqual(len(rows), 1)
            self.assertEqual(rows[0].seller, "Cafe")

