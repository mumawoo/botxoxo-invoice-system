    def test_generates_manual_checked_workbook_for_human_review(self):
        with tempfile.TemporaryDirectory() as temp:
            temp_path = Path(temp)
            out = temp_path / "Invoice_Output.xlsx"
            manual = temp_path / "Invoice_Manually_Checked.xlsx"
            store = InvoiceWorkbook(out, manual)

            store.write_records([InvoiceRecord(invoice_date="2026-06-12", seller="Cafe", total_amount=10)])

            self.assertTrue(out.exists())
            self.assertTrue(manual.exists())
            wb = load_workbook(manual, data_only=True)
            self.assertEqual([cell.value for cell in wb.active[1]], EXCEL_HEADERS)
            self.assertEqual(wb.active.cell(2, 11).value, "Cafe")
            wb.close()

